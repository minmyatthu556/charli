let a1click = document.getElementById('a1-click');
let a1tracks = document.getElementById('a1-tracklist');
a1tracks.style.display = "none";

a1click.onclick = function () {
    if (a1tracks.style.display === "none") {
        a1tracks.style.display = "block";
    } else {
        a1tracks.style.display = "none";
    }
}

